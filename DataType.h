#ifndef _DATA_TYPE_H_
#define _DATA_TYPE_H_

/*!
@brief This enum defines the data type of a Data
*/
enum DataType
{
	/*!
	@brief unknown data type
	*/
	UNKNOWN,//unknown data type

	/*!
	 * @ brief int data type
	 */
	INTDATA,

	/*!
	 * @ brief string data type
	 */
	STRINGDATA,

	/*!
	 * @ brief double data type
	 */
	DOUBLEDATA
};

#endif

